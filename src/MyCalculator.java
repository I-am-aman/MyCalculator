    private void isPrimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_isPrimeActionPerformed
        // TODO add your handling code here:
        String s=display.getText();
        long numm=(long)Integer.parseInt(s);
        int flag=0;
        for(long i=2;i*i<=numm;i++)
        {
            if(numm%i==0)
            {flag=1;break;}
        
        }
        if(flag==0)
        {
            display.setText(numm+" is prime");
        }
        else
        {
            display.setText(numm+" is not a prime");
        }
    }//GEN-LAST:event_isPrimeActionPerformed

    private void isArmstrongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_isArmstrongActionPerformed
        // TODO add your handling code here:
        String s=display.getText();
        long numm=(long)Integer.parseInt(s);
        int flag=0;
        long summ=0;
        long cp=numm;
        while(numm!=0)
        {
            long x=(long)numm%10;
            summ=summ+(x*x*x);
            numm/=10;
        }
        if(summ==cp)
        {
            display.setText(cp+" is armstrong number");
        }
        else
        {
            display.setText(cp+" is not armstrong number");
        }
    }//GEN-LAST:event_isArmstrongActionPerformed

