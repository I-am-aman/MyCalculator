    public void arithmetic()
    {
        switch(cal)
        {
            case 1: ans = num + Double.parseDouble(display.getText());
                    display.setText(Double.toString(ans));
                    break;
                    
            case 2: ans = num - Double.parseDouble(display.getText());
                    display.setText(Double.toString(ans));
                    break;
                    
            case 3: ans = num * Double.parseDouble(display.getText());
                    display.setText(Double.toString(ans));
                    break;
                    
            case 4: double num2=Double.parseDouble(display.getText());
                    if(num2==0.00)
                    {
                        display.setText("cannot divide by zero");
                        break;
                    }
                    ans = num / Double.parseDouble(display.getText());
                    display.setText(Double.toString(ans));
                    break;
                    
            case 5: ans = 1/num ;
                    display.setText(Double.toString(ans));
                    break;
                    
            case 6: ans = num*num;
                    display.setText(Double.toString(ans));
                    break;
        }
    }
    
